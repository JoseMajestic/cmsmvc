    public function redireccionConMensaxe($ruta, $tipo, $texto)
    {

        $_SESSION['mensaxe'] = array("tipo" => $tipo, "texto" => $texto);
        header("Location:" . $_SESSION["home"] . $ruta);

    }

    public function permisos($permiso = null)
    {

        if (isset($_SESSION['usuario']) and ($permiso == null or $_SESSION[$permiso] == 1)) {
            return true;
        } else {
            $this->redireccionConMensaxe("admin", "yellow", "No tienes permiso para realizar esta operaci贸n");
        }

